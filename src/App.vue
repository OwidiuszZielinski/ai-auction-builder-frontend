  <div class="app">
    <input
      v-model="text"
      class="input"
      placeholder="TYPE HERE"
      maxlength="30"
    />

    <div class="grid">
      <div
        v-for="(cell, i) in grid"
        :key="i"
        class="cell"
        :class="{ active: cell }"
