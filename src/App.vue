<template>
  <div class="app">
    <input
      v-model="text"
      class="input"
      placeholder="WPISZ TEKST"
    />

    <div
      class="grid"
      :style="{ gridTemplateColumns: `repeat(${cols}, ${CELL}px)` }"
    >
      <div
        v-for="(cell, i) in grid"
        :key="i"
        class="cell"
        :class="{ active: cell }"
      />
    </div>
  </div>
</template>

<script setup>
import { computed, ref } from "vue";

/* ===== CONFIG ===== */
const ROWS = 7;
const CELL = 12;
const GAP = 4;
const CHAR_WIDTH = 6; // 5px + 1 odstÄ™p
const MIN_COLS = 90;

/* ===== STATE ===== */
const text = ref("WEZ SIE ZA ROBOTE");

/* ===== FONT 5x7 ===== */
const FONT = {
  A:["01110","10001","10001","11111","10001","10001","10001"],
  B:["11110","10001","10001","11110","10001","10001","11110"],
  C:["01111","10000","10000","10000","10000","10000","01111"],
  D:["11110","10001","10001","10001","10001","10001","11110"],
  E:["11111","10000","10000","11110","10000","10000","11111"],
  F:["11111","10000","10000","11110","10000","10000","10000"],
  G:["01111","10000","10000","10111","10001","10001","01111"],
  H:["10001","10001","10001","11111","10001","10001","10001"],
  I:["11111","00100","00100","00100","00100","00100","11111"],
  J:["00111","00010","00010","00010","10010","10010","01100"],
  K:["10001","10010","10100","11000","10100","10010","10001"],
  L:["10000","10000","10000","10000","10000","10000","11111"],
  M:["10001","11011","10101","10101","10001","10001","10001"],
  N:["10001","11001","10101","10011","10001","10001","10001"],
  O:["01110","10001","10001","10001","10001","10001","01110"],
  P:["11110","10001","10001","11110","10000","10000","10000"],
  Q:["01110","10001","10001","10001","10101","10010","01101"],
  R:["11110","10001","10001","11110","10100","10010","10001"],
  S:["01111","10000","10000","01110","00001","00001","11110"],
  T:["11111","00100","00100","00100","00100","00100","00100"],
  U:["10001","10001","10001","10001","10001","10001","01110"],
  V:["10001","10001","10001","10001","10001","01010","00100"],
  W:["10001","10001","10001","10101","10101","10101","01010"],
  X:["10001","10001","01010","00100","01010","10001","10001"],
  Y:["10001","10001","01010","00100","00100","00100","00100"],
  Z:["11111","00001","00010","00100","01000","10000","11111"],
  "0":["01110","10001","10011","10101","11001","10001","01110"],
  "1":["00100","01100","00100","00100","00100","00100","01110"],
  "2":["01110","10001","00001","00010","00100","01000","11111"],
  "3":["11110","00001","00001","01110","00001","00001","11110"],
  "4":["00010","00110","01010","10010","11111","00010","00010"],
  "5":["11111","10000","10000","11110","00001","00001","11110"],
  "6":["01110","10000","10000","11110","10001","10001","01110"],
  "7":["11111","00001","00010","00100","01000","01000","01000"],
  "8":["01110","10001","10001","01110","10001","10001","01110"],
  "9":["01110","10001","10001","01111","00001","00001","01110"],
  " ":["00000","00000","00000","00000","00000","00000","00000"],
};

/* ===== GRID WIDTH ===== */
const cols = computed(() => {
  const needed = text.value.length * CHAR_WIDTH;
  return Math.max(MIN_COLS, needed);
});

/* ===== RENDER ===== */
const grid = computed(() => {
  const matrix = Array.from({ length: ROWS }, () =>
    Array(cols.value).fill(false)
  );

  const chars = text.value.toUpperCase();
  const textWidth = chars.length * CHAR_WIDTH - 1;
  let xOffset = Math.floor((cols.value - textWidth) / 2);

  for (const ch of chars) {
    const glyph = FONT[ch] || FONT[" "];

    for (let y = 0; y < ROWS; y++) {
      for (let x = 0; x < 5; x++) {
        if (glyph[y][x] === "1" && xOffset + x >= 0) {
          matrix[y][xOffset + x] = true;
        }
      }
    }
    xOffset += CHAR_WIDTH;
  }

  return matrix.flat();
});
</script>

